package solo.four;

public class num2 {
    public static void main(String[] args) {
        int num = 2;
        int count = 1;
        while (count <= 10) {
            System.out.println(num * count);
            count++;
        }

        count = 1;
        for (int i = 1; count <= 10; i++) {
            System.out.println(i*2);
            count++;
        }

    }

}
